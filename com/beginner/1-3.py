# Python的注释

# 任何时候，我们都可以给程序加上注释。注释是用来说明代码的，给自己或别人看，而程序运行的时候，Python解释器会直接忽略掉注释，所以，有没有注释不影响程序的执行结果，但是影响到别人能不能看懂你的代码。

# Python的注释以 # 开头，后面的文字直到行尾都算注释

# # 这一行全部都是注释...
# print 'hello' # 这也是注释

# 注释还有一个巧妙的用途，就是一些代码我们不想运行，但又不想删除，就可以用注释暂时屏蔽掉：

# # 暂时不想运行下面一行代码:
# # print 'hello, python.'

